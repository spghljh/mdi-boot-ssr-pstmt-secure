package kr.co.mdi.cpu.dto;

public class CoreStatDTO {
	private int coreCpu;
	private int count;

	public int getCoreCpu() {
		return coreCpu;
	}

	public void setCoreCpu(int coreCpu) {
		this.coreCpu = coreCpu;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
